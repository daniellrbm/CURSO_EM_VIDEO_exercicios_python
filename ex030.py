# Crie um programa que leia um número inteiro e mostre na tela se ele é PAR ou ÍMPAR.

print('PAR OU ÍMPAR???')
num = int(input('Digite um número: '))

if num % 2 == 0:
    print('o número {} é par.'.format(num))
else:
    print('o número {} é ímpar.'.format(num))
